;=================================================================
; screen.asm
;
; Screen utility routines
;=================================================================

;-----------------------------------------------------------------
; Clear screen by setting 0xFF in all DMA memory
;-----------------------------------------------------------------
ClearScreen:
  ldx #$00
  lda #$FF
ClsLoop:
  sta $0200,x
  inx
  cpx #$FF
  bne ClsLoop
  rts
  
;-----------------------------------------------------------------
; Wait for VBlank signal
;-----------------------------------------------------------------
WaitVBlank:
  bit $2002
  bpl WaitVBlank
  rts